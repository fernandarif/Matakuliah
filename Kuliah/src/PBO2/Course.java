package PBO2;

// Kelas untuk merepresentasikan mata kuliah
public class Course {
    private String name; // Atribut untuk menyimpan nama mata kuliah

    // Constructor untuk menginisialisasi nama mata kuliah
    public Course(String name) {
        this.name = name;
    }

    // Getter untuk mengambil nama mata kuliah
    public String getName() {
        return name;
    }
}
